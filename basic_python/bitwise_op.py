#bitwise AND.....
a=0b1010
b=0b1100
c=a & b
print(bin(c))

#bitwise OR......
a=0b1010
b=0b1100
c=a | b
print(bin(c))

#bitwise XOR......
a=0b1010
b=0b1100
c=a ^ b
print(bin(c))

#bitwise NOT......
a=0b1010
c= ~a
print(bin(c))

#bitwise LEFT SHIFT.....
a=0b1010
c=a<<2
print(bin(c))

#bitwise RIGHT SHIFT.....
a=0b1010
c=a>>1
print(bin(c))

